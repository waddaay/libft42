#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (!s)
		write (fd, NULL, i);
	else
	{
		while (s[i])
			i++;
		write (fd, s, i);
	}
}
